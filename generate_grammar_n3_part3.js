let content = fs.readFileSync('data_n3.js', 'utf8');

const ctx = { window: { JLPT_DATA_CHUNKS: {} } };
const vm = require('vm');
vm.createContext(ctx);
vm.runInContext(content, ctx);

const data = ctx.window.JLPT_DATA_CHUNKS['N3'];

let startIdx = data.grammar.length + 1;
newGrammarRaw.forEach((g) => {
  data.grammar.push({
    id: 'g_n3_' + startIdx++,
    title: g.title,
    structure: g.structure,
    explanation: g.explanation,
    examples: g.examples,
    level: 'N3'
  });
});

const newData = `window.JLPT_DATA_CHUNKS = window.JLPT_DATA_CHUNKS || {};\nwindow.JLPT_DATA_CHUNKS["N3"] = ${JSON.stringify(data, null, 2)};\n`;
fs.writeFileSync('data_n3.js', newData, 'utf8');

console.log('N3 grammar part 3 generated successfully. Total: ' + data.grammar.length + ' items');
