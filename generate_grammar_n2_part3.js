vm.createContext(ctx);
vm.runInContext(content, ctx);

const data = ctx.window.JLPT_DATA_CHUNKS['N2'];

let startIdx = data.grammar.length + 1;
newGrammarRaw.forEach((g) => {
  data.grammar.push({
    id: 'g_n2_' + startIdx++,
    title: g.title,
    structure: g.structure,
    explanation: g.explanation,
    examples: g.examples,
    level: 'N2'
  });
});

